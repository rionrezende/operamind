# ── System prompt ──────────────────────────────────────────────────────────

SYSTEM_PROMPT = """You are OperaMind's AI Operations Employee.

You are the nervous system of the business. Your job is to:
1. Monitor all workflows and KPIs continuously
2. Detect SLA breaches and at-risk tickets — alert immediately via Slack
3. Generate daily standup digests and weekly KPI reports automatically
4. Assign tasks when bottlenecks are found
5. Monitor workflow health — report failures and retries

Priority order:
1. CRITICAL alerts (SLA breaches, workflow failures, cash flow risks) → send Slack immediately
2. WARNING items → include in next digest, assign tasks
3. INFO → include in weekly report

Always check all departments before generating a report.
Current date: {date} {time}
"""

# ── Graph ──────────────────────────────────────────────────────────────────

tools = [fetch_kpis, check_sla_compliance, generate_ops_report,
         send_slack_alert, assign_task, monitor_workflow_health]
tool_node = ToolNode(tools)
llm_with_tools = llm.bind_tools(tools)

def ops_agent(state: OpsState) -> OpsState:
    now = datetime.datetime.utcnow()
    system = SYSTEM_PROMPT.format(date=now.date(), time=now.strftime("%H:%M UTC"))
    response = llm_with_tools.invoke([SystemMessage(content=system)] + state["messages"])
    return {"messages": [response]}

def router(state: OpsState) -> Literal["tools", "end"]:
    last = state["messages"][-1]
    return "tools" if (hasattr(last, "tool_calls") and last.tool_calls) else "end"

checkpointer = MemorySaver()
builder = StateGraph(OpsState)
builder.add_node("agent", ops_agent)
builder.add_node("tools", tool_node)
builder.add_edge(START, "agent")
builder.add_conditional_edges("agent", router, {"tools": "tools", "end": END})
builder.add_edge("tools", "agent")
graph = builder.compile(checkpointer=checkpointer)

# ── Public API ─────────────────────────────────────────────────────────────

def run_morning_check(scope: str = "all") -> dict:
    """Run the daily operations morning check — called automatically at 8 AM."""
    config = {"configurable": {"thread_id": f"morning_{datetime.date.today()}"}}
    prompt = (
        f"Run the morning operations check for {scope}.\n"
        "1. Fetch all KPIs\n"
        "2. Check SLA compliance for support\n"
        "3. Monitor workflow health\n"
        "4. Send any critical alerts to #ops-alerts on Slack\n"
        "5. Generate a daily standup digest and send to #team-standup"
    )
    result = graph.invoke(
        {"messages": [HumanMessage(content=prompt)], "monitoring_scope": scope,
         "alerts_triggered": [], "reports_generated": [],
         "last_check": datetime.datetime.utcnow().isoformat()},
        config=config,
    )
    final = next((m for m in reversed(result["messages"])
                  if isinstance(m, AIMessage) and not m.tool_calls), None)
    return {"ops_summary": final.content if final else "Check complete."}

def trigger_alert(alert_type: str, details: str) -> dict:
    """Manually trigger an operations alert (called by other agents or webhooks)."""
    config = {"configurable": {"thread_id": f"alert_{datetime.datetime.utcnow().timestamp():.0f}"}}
    prompt = f"ALERT TRIGGERED: {alert_type}\nDetails: {details}\nTake appropriate action immediately."
    result = graph.invoke(
        {"messages": [HumanMessage(content=prompt)], "monitoring_scope": "alert",
         "alerts_triggered": [], "reports_generated": [],
         "last_check": datetime.datetime.utcnow().isoformat()},
        config=config,
    )
    final = next((m for m in reversed(result["messages"])
                  if isinstance(m, AIMessage) and not m.tool_calls), None)
    return {"alert_handled": True, "response": final.content if final else "Alert processed."}


