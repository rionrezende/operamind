# ── Graph ──────────────────────────────────────────────────────────────────

tools = [fetch_kpis, check_sla_compliance, generate_ops_report,
         send_slack_alert, assign_task, monitor_workflow_health,
         forecast_capacity, detect_bottlenecks, schedule_recurring_sync,
         track_okrs, create_incident_report]
tool_node = ToolNode(tools)
llm_with_tools = llm.bind_tools(tools)

def ops_agent(state: OpsState) -> OpsState:
    now = datetime.datetime.utcnow()
    system = SYSTEM_PROMPT.format(date=now.date(), time=now.strftime("%H:%M UTC"))
    response = llm_with_tools.invoke([SystemMessage(content=system)] + state["messages"])
    return {"messages": [response]}

def router(state: OpsState) -> Literal["tools", "end"]:
    last = state["messages"][-1]
    return "tools" if (hasattr(last, "tool_calls") and last.tool_calls) else "end"

checkpointer = MemorySaver()
builder = StateGraph(OpsState)
builder.add_node("agent", ops_agent)
builder.add_node("tools", tool_node)
builder.add_edge(START, "agent")
builder.add_conditional_edges("agent", router, {"tools": "tools", "end": END})
builder.add_edge("tools", "agent")
graph = builder.compile(checkpointer=checkpointer)

