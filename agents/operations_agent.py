def run_morning_check(scope: str = "all") -> dict:
    """Run the daily operations morning check — called automatically at 8 AM."""
    config = {"configurable": {"thread_id": f"morning_{datetime.date.today()}"}}
    prompt = (
        f"Run the morning operations check for {scope}.\n"
        "1. Fetch all KPIs\n"
        "2. Check SLA compliance for support\n"
        "3. Monitor workflow health\n"
        "4. Send any critical alerts to #ops-alerts on Slack\n"
        "5. Generate a daily standup digest and send to #team-standup"
    )
    result = graph.invoke(
        {"messages": [HumanMessage(content=prompt)], "monitoring_scope": scope,
         "alerts_triggered": [], "reports_generated": [],
         "last_check": datetime.datetime.utcnow().isoformat()},
        config=config,
    )
    final = next((m for m in reversed(result["messages"])
                  if isinstance(m, AIMessage) and not m.tool_calls), None)
    return {"ops_summary": final.content if final else "Check complete."}

def trigger_alert(alert_type: str, details: str) -> dict:
    """Manually trigger an operations alert (called by other agents or webhooks)."""
    config = {"configurable": {"thread_id": f"alert_{datetime.datetime.utcnow().timestamp():.0f}"}}
    prompt = f"ALERT TRIGGERED: {alert_type}\nDetails: {details}\nTake appropriate action immediately."
    result = graph.invoke(
        {"messages": [HumanMessage(content=prompt)], "monitoring_scope": "alert",
         "alerts_triggered": [], "reports_generated": [],
         "last_check": datetime.datetime.utcnow().isoformat()},
        config=config,
    )
    final = next((m for m in reversed(result["messages"])
                  if isinstance(m, AIMessage) and not m.tool_calls), None)
    return {"alert_handled": True, "response": final.content if final else "Alert processed."}


