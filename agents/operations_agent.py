    print(f"[SLACK] → #{channel}: {full_msg}")
    # Production: slack_sdk.WebClient(token=os.environ["SLACK_BOT_TOKEN"]).chat_postMessage(...)
    return {
        "sent": True,
        "channel": channel,
        "urgency": urgency,
        "timestamp": datetime.datetime.utcnow().isoformat(),
    }

