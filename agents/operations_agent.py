@tool
def send_slack_alert(
    channel: str,
    message: str,
    urgency: Literal["info", "warning", "critical"] = "info",
    mention: str = "",
) -> dict:
    """
    Send an alert to a Slack channel.
    urgency: info (blue) | warning (yellow) | critical (red)
    """
    emoji = {"info": "ℹ️", "warning": "⚠️", "critical": "🚨"}.get(urgency, "ℹ️")
    full_msg = f"{emoji} {mention + ' ' if mention else ''}{message}"
    print(f"[SLACK] → #{channel}: {full_msg}")
    # Production: slack_sdk.WebClient(token=os.environ["SLACK_BOT_TOKEN"]).chat_postMessage(...)
    return {
        "sent": True,
        "channel": channel,
        "urgency": urgency,
        "timestamp": datetime.datetime.utcnow().isoformat(),
    }

